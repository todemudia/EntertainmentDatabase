import { Schema,model } from 'mongoose';

const UserSchema = new Schema ({
    name: {
        type:String,
        required: true
    },
    lastname: {
        type:String,
        required: true
    },
    email: {
        type:String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    register_date: {
        type: Date,
        default: Date.now
    },
})

const User = model('User', userSchema);

export default User;